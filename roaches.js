function getRandomInt(max) {
    return Math.floor(Math.random() * max);
}

function roachBehindImage(roach, image) {
    if (!image.complete || image.naturalHeight === 0) {
        // the image hasn't loaded yet
        return false;
    }
    const roachRect = roach.getBoundingClientRect();
    const imageRect = image.getBoundingClientRect();

    // the BoundingClientRect includes padding, roaches cannot hide in the
    // padding, so the padding needs to be subtracted in the calculation
    var imageStyle = getComputedStyle(image);

    return roachRect.top > (imageRect.top + parseFloat(imageStyle.paddingTop))
            && roachRect.bottom < (imageRect.bottom - parseFloat(imageStyle.paddingBottom))
            && roachRect.left > (imageRect.left + parseFloat(imageStyle.paddingLeft))
            && roachRect.right < (imageRect.right - parseFloat(imageStyle.paddingRight));
}

class Roach {
    constructor(name) {
        this.name = name;
        this.dir = getRandomInt(24) * 15;
        this.x = getRandomInt(window.innerWidth);
        this.y = getRandomInt(window.innerHeight);
        this.speed = 20;
        this.stoppedMoving = false;

        this.element = document.createElement("img");
        this.element.style="position:absolute; z-index:-1000;";

        document.body.appendChild(this.element);
    }

    roachIsHidden() {
        var hidden = false;
        document.querySelectorAll('img').forEach(image => {
                if (roachBehindImage(this.element, image)) {
                    hidden = true;
                    this.stoppedMoving = true;
                }
            }
        );
        return hidden;
    }

    moveRoach() {
        const dirModifier = getRandomInt(3) - 1;
        this.dir = (this.dir + dirModifier * 15 + 360) % 360;
        this.x = this.x + this.speed * Math.cos((this.dir * (Math.PI/180)));
        this.y = this.y - this.speed * Math.sin(this.dir * (Math.PI/180));
    }

    wrapAroundBorders() {
        const spacing = -50;
        if (this.x < 0) {
            this.x = window.innerWidth + spacing;
        }
        if (this.x > window.innerWidth + spacing) {
            this.x = 0;
        }
        if (this.y < 0) {
            this.y = window.innerHeight + spacing;
        }
        if (this.y > window.innerHeight + spacing) {
            this.y = 0;
        }
    }

    move() {
        if (this.roachIsHidden()) {
            return;
        }

        this.moveRoach();
        this.wrapAroundBorders();
    }

    redraw() {
        this.element.style.left = this.x + 'px';
        this.element.style.top = window.pageYOffset + this.y + 'px';
        this.element.src=this.getImg();
    }

    getImg() {
        return eval('roach' + this.dir.toString().padStart(3, '0'));
    }
}

function roachesAreGo(roachCount) {
    this.roaches = new Set();
    for (var i = 0; i < roachCount; i++) {
        this.roaches.add(new Roach("roach #" + i));
    }
    const interval = setInterval(function() {
        this.roaches.forEach((roach) => {
            roach.move();
            roach.redraw();
        });
    }, 50);
}

